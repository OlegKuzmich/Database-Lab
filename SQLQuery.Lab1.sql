-- Послідовність для моделей літаків
CREATE SEQUENCE Кузьмич.Seq_Моделі
    START WITH 1
    INCREMENT BY 1;

-- Послідовність для табельних номерів екіпажу
CREATE SEQUENCE Кузьмич.Seq_Екіпаж
    START WITH 100
    INCREMENT BY 1;
GO

-- Прив'язка до МоделіЛітаків
ALTER TABLE Кузьмич.МоделіЛітаків 
ADD CONSTRAINT DF_Моделі_ID DEFAULT (NEXT VALUE FOR Кузьмич.Seq_Моделі) FOR МодельID;

-- Прив'язка до Екіпаж
ALTER TABLE Кузьмич.Екіпаж 
ADD CONSTRAINT DF_Екіпаж_ID DEFAULT (NEXT VALUE FOR Кузьмич.Seq_Екіпаж) FOR ТабельнийНомер;
