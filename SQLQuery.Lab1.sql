    КількістьМісць INT NOT NULL,
    Вантажопідйомність INT NOT NULL
);
GO


CREATE TABLE Кузьмич.Екіпаж (
    ТабельнийНомер INT PRIMARY KEY,
    Прізвище NVARCHAR(100) NOT NULL,
    ДатаНародження DATE,
    Адреса NVARCHAR(255),
    Посада NVARCHAR(50), -- Пілот, Командир, Стюардеса
);
GO


CREATE TABLE Кузьмич.Літаки (
    БортовийНомер NVARCHAR(50) PRIMARY KEY,
    МодельID INT NOT NULL,
    КількістьГодин INT DEFAULT 0,

    CONSTRAINT FK_Літаки_Моделі FOREIGN KEY (МодельID)
        REFERENCES Кузьмич.МоделіЛітаків(МодельID)
)
GO


CREATE TABLE Кузьмич.Рейси(
    НомерРейсу INT PRIMARY KEY,
    ПунктВідправлення NVARCHAR(50),
    ПунктПризначення NVARCHAR(50),
    ЧасВильоту DATE,
    ЧасПосадки DATE,
    КількістьКвитків INT,
    МодельID INT NOT NULL,
    БортовийНомер NVARCHAR(50) NOT NULL,

    CONSTRAINT FK_Рейси_Моделі FOREIGN KEY (МодельID)
        REFERENCES Кузьмич.МоделіЛітаків(МодельID),

    CONSTRAINT FK_Рейси_БортовийНомер FOREIGN KEY (БортовийНомер)
        REFERENCES Кузьмич.Літаки(БортовийНомер)
)
GO


