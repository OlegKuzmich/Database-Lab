--------------------------------------------------------------------------
-- 1. SELECT на базі однієї таблиці (Сортування, умови OR та AND)
--------------------------------------------------------------------------
-- Виведемо екіпаж (пілотів або командирів), які живуть у Києві, за алфавітом
SELECT * FROM Екіпаж 
WHERE (Посада = N'Пілот' OR Посада = N'Командир') AND Адреса LIKE N'%Київ%'
ORDER BY Прізвище ASC;

--------------------------------------------------------------------------
-- 2. SELECT з виводом обчислюваних полів (виразів)
--------------------------------------------------------------------------
-- Рахуємо відсоток заповненості рейсу (припустимо, що середня місткість 150)
SELECT НомерРейсу, 
       КількістьКвитків, 
       (КількістьКвитків * 100.0 / 150.0) AS ВідсотокЗавантаження
FROM Рейси;

--------------------------------------------------------------------------
-- 3. SELECT на базі кількох таблиць (Inner Join, Sort, OR/AND)
--------------------------------------------------------------------------
-- Виведемо рейс, модель літака та його наліт для рейсів у Варшаву або Париж
SELECT R.НомерРейсу, M.НазваМоделі, L.КількістьГодин
FROM Рейси R
JOIN Літаки L ON R.БортовийНомер = L.БортовийНомер
JOIN Моделі M ON L.МодельID = M.МодельID
WHERE R.ПунктПризначення = N'Варшава' OR R.ПунктПризначення = N'Париж'
ORDER BY L.КількістьГодин DESC;

--------------------------------------------------------------------------
-- 4. SELECT з типом поєднання Outer Join
--------------------------------------------------------------------------
-- Виведемо всі моделі літаків і наявні борти (навіть якщо борта для моделі ще немає)
SELECT M.НазваМоделі, L.БортовийНомер
FROM Моделі M
LEFT OUTER JOIN Кузьмич.Літаки L ON M.МодельID = L.МодельID;

--------------------------------------------------------------------------
-- 5. Використання операторів Like, Between, In, Exists
--------------------------------------------------------------------------
-- Знайти рейси, де продано від 50 до 150 квитків, і на які вже призначено екіпаж
SELECT * FROM Рейси R
  WHERE КількістьКвитків BETWEEN 50 AND 150
   AND ПунктПризначення IN (N'Лондон', N'Стамбул', N'Відень')
   AND EXISTS (SELECT 1 FROM ПризначенняЕкіпажу PE WHERE PE.НомерРейсу = R.НомерРейсу);

--------------------------------------------------------------------------
-- 6. SELECT з використанням підсумовування та групування (Aggregate)
--------------------------------------------------------------------------
-- Порахуємо кількість рейсів та загальну кількість квитків для кожного міста
SELECT ПунктПризначення, 
       COUNT(НомерРейсу) AS КількістьРейсів, 
       SUM(КількістьКвитків) AS ВсьогоПродано
FROM Рейси
GROUP BY ПунктПризначення
HAVING COUNT(НомерРейсу) >= 1;

--------------------------------------------------------------------------
-- 7. SELECT з використанням під-запитів (Where та From)
--------------------------------------------------------------------------
-- Підзапит у WHERE: Знайти екіпаж, який має допуск до моделі ID = 1
SELECT Прізвище, Посада 
FROM Екіпаж
WHERE ТабельнийНомер IN (SELECT ТабельнийНомер FROM ДопускиПілотів WHERE МодельID = 1);

-- Підзапит у FROM: Вибірка з віртуальної таблиці лише пілотів
SELECT TempTable.Прізвище 
