--------------------------------------------------------------------------
-- 8. Спеціальні типи SELECT (Ієрархічний та CrossTab)
--------------------------------------------------------------------------
-- Ієрархічний запит (простий приклад через СТЕ)
WITH Hierarchy AS (
    SELECT ТабельнийНомер, Прізвище, Посада, 1 AS Рівень FROM Кузьмич.Екіпаж WHERE Посада = N'Командир'
    UNION ALL
    SELECT E.ТабельнийНомер, E.Прізвище, E.Посада, H.Рівень + 1 
    FROM Екіпаж E JOIN Hierarchy H ON E.Посада = N'Пілот' WHERE H.Рівень = 1
)
SELECT * FROM Hierarchy;

-- Приклад CrossTab (Pivoting): Кількість рейсів за пунктами призначення (у стовпці)
SELECT N'Кількість рейсів' AS Показник, [Київ], [Львів], [Одеса]
FROM (SELECT ПунктПризначення, НомерРейсу FROM Кузьмич.Рейси) AS SourceTable
PIVOT (COUNT(НомерРейсу) FOR ПунктПризначення IN ([Київ], [Львів], [Одеса])) AS PivotTable;

