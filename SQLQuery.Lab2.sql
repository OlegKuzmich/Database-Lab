-- UPDATE однієї таблиці (виправляємо NULL дати)
UPDATE Екіпаж SET ДатаНародження = '1990-01-01' WHERE ДатаНародження IS NULL;

-- UPDATE кількох таблиць (Збільшуємо квитки для рейсів, де літак має великий наліт)
UPDATE Кузьмич.Рейси
SET КількістьКвитків = КількістьКвитків + 5
FROM Кузьмич.Рейси R
JOIN Кузьмич.Літаки L ON R.БортовийНомер = L.БортовийНомер
WHERE L.КількістьГодин > 500;

