-- СКЛАДНИЙ ЗАПИТ (Об'єднує Join, Group By, Having та агрегати)
--------------------------------------------------------------------------
-- Вивести пілотів, які літають більше ніж на 2 різних рейсах
SELECT E.Прізвище, COUNT(PE.НомерРейсу) AS ВсьогоРейсів
FROM Екіпаж E
JOIN ПризначенняЕкіпажу PE ON E.ТабельнийНомер = PE.ТабельнийНомер
GROUP BY E.Прізвище
HAVING COUNT(PE.НомерРейсу) > 2;